/**
 * Get countries by continent
 */
export function getCountriesByContinent(continent: 'Africa' | 'Americas' | 'Asia' | 'Europe' | 'Oceania'): CountrySeedData[] {
  return COUNTRIES_SEED_DATA.filter(c => c.continent === continent);
}

/**
 * Get countries by data source coverage
 */
export function getCountriesByDataCoverage(coverage: 'excellent' | 'good' | 'limited' | 'minimal'): CountrySeedData[] {
  return COUNTRIES_SEED_DATA.filter(c => c.data_source_coverage === coverage);
}

/**
 * Get enabled countries only
 */
export function getEnabledCountries(): CountrySeedData[] {
  return COUNTRIES_SEED_DATA.filter(c => c.enabled);
}

/**
 * Get country by code
 */
export function getCountryByCode(code: string): CountrySeedData | undefined {
  const upperCode = code.toUpperCase();
  return COUNTRIES_SEED_DATA.find(
    c => c.country_code === upperCode || c.country_code_alpha3 === upperCode
  );
}

/**
 * Get countries with free company registry APIs
 */
export function getCountriesWithFreeAPIs(): CountrySeedData[] {
  return COUNTRIES_SEED_DATA.filter(c => c.company_registry_type === 'free_api');
}

/**
 * Get major economies (GDP > $500B)
 */
export function getMajorEconomies(): CountrySeedData[] {
  return COUNTRIES_SEED_DATA.filter(c => c.gdp_usd && c.gdp_usd > 500000000000);
}
