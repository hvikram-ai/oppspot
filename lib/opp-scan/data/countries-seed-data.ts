/**
 * Country Metadata Seed Data
 *
 * Comprehensive metadata for all 195 UN-recognized countries
 * Used to populate the countries table in Supabase
 *
 * Data sources:
 * - ISO 3166-1 country codes
 * - World Bank open data
 * - Transparency International CPI
 * - World Bank Doing Business Index
 * - OpenCorporates coverage data
 *
 * Last updated: 2025-01-12
 */

export interface CountrySeedData {
  country_code: string;              // ISO 3166-1 alpha-2
  country_code_alpha3: string;       // ISO 3166-1 alpha-3
  numeric_code: string;              // ISO 3166-1 numeric
  name: string;
  official_name?: string;
  native_name?: string;

  // Geographic
  continent: 'Africa' | 'Americas' | 'Asia' | 'Europe' | 'Oceania';
  region?: string;
  capital?: string;
  latitude?: number;
  longitude?: number;
  area_sq_km?: number;

  // Currency & Economic
  currency_code: string;             // ISO 4217
  currency_name?: string;
  currency_symbol?: string;

  // Time & Language
  timezone?: string;
  utc_offset?: string;
  languages?: string[];

  // Business & Economic Data
  population?: number;
  gdp_usd?: number;
  gdp_per_capita_usd?: number;
  business_density?: 'low' | 'moderate' | 'high' | 'very_high';
  ease_of_business_score?: number;   // 0-100
  corporate_tax_rate?: number;
  vat_gst_rate?: number;

  // Regulatory & Compliance
  regulatory_complexity?: 'low' | 'moderate' | 'high' | 'very_high';
  legal_system?: 'common_law' | 'civil_law' | 'mixed' | 'customary' | 'religious';
  corruption_perception_index?: number; // 0-100
  geopolitical_risk?: 'minimal' | 'low' | 'moderate' | 'high' | 'very_high';

  // Industry & Trade
  key_industries?: string[];
  trade_agreements?: string[];
  foreign_investment_restrictions?: string;

  // Data Sources Available
  has_company_registry: boolean;
  company_registry_url?: string;
  company_registry_type?: 'free_api' | 'paid_api' | 'web_scraping' | 'none';
  company_registry_notes?: string;
  data_source_coverage: 'excellent' | 'good' | 'limited' | 'minimal';

  // Metadata
  enabled: boolean;
  notes?: string;
}

